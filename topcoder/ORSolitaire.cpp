class ORSolitaire {
public:
	int getMinimum(vector <int>, int);
};

int ORSolitaire::getMinimum(vector <int> numbers, int goal) {
	int res = numbers.size() ;
    for (int i = 0; i < 31; i++) {
        if ( goal & (1<<i) ) {
            int c = 0;
            for (int x : numbers) {
                if ( (x & (1<<i)) && ( (goal & x) == x) ) {
                    c++;
                }
            }
            res = std::min(res, c);
        }
    }
    return res;
}


