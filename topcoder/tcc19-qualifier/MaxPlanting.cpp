class MaxPlanting {
public:
	int most(int, int, int, int);
};

int MaxPlanting::most(int width, int height, int size, int max) {
	if(max >= size * size) return height * width;
	int submat[size][size], ret=0, cnt = 0, sz=0;
	memset(submat, 0, sizeof(submat));
	while(sz * sz <= max) sz++;
	sz--;
	for(int i = 0; i < sz; i++) {
		for(int j = 0; j < sz; j++) {
			submat[i][j] = 1;
			cnt++; 
		}
	}
	max -= sz * sz;
	while(max > 0) {
		int rc = 0, cr = 0;
		while(max) {
			submat[sz][rc]=1; rc++;
			max--;
			if(max) {
				submat[cr][sz] = 1; cr++;
				max--;
			}		
		}
	}
	for(int i = 0; i < height; i++) {
		for(int j = 0; j < width; j++) {
			int ri = i % size;
			int rj = j % size;
			if(submat[ri][rj]) {
				ret++;
			}
		}
	}
	return ret;
}


